/// <summary>
/// The Job enum lists the jobs available for an imp.
/// An imp must always have a job.
/// The default setting is 'Unemployed'.
/// </summary>

public enum ImpType
{
    Unemployed, // Default
    Spearman,
    Guardian,
    Slingshot,
    LadderCarrier,
    Blaster,
    Firebug,
    Mechanic,
    Bard,
    Schwarzenegger
}